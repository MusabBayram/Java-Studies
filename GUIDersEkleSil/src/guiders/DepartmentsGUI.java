    private void fillDepartmentsjList()
    {
        try {
            
            DefaultListModel<Item> model = new DefaultListModel();
            String connection = "jdbc:hsqldb:file:db/Faculty";
            String sql = "SELECT * FROM TBLDEPARTMENT";
            DBHandler db = new DBHandler(connection);
            ResultSet rs = db.executeQuery(sql);
            while(rs.next())
            {
                model.addElement(new Item(rs.getInt("DEPID"),rs.getString("DEPAD")));
            }
            jListDepartments.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(DepartmentsGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
