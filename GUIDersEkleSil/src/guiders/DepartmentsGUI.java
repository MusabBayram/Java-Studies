    private void jButtonDepAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonDepAddMouseClicked
        try {
            String connection = "jdbc:hsqldb:file:db/Faculty";
            String sql = "SELECT COUNT(*) FROM TBLDEPARTMENT WHERE DEPAD = '" + jTextFieldDepName.getText() +"'" ;
            System.out.println(sql);
            DBHandler db = new DBHandler(connection);
            
            if(jTextFieldDepName.getText().isEmpty())
                JOptionPane.showMessageDialog(null, "Bölüm adı boş olamaz");
            else
            {
                ResultSet rs = db.executeQuery(sql);
                int count = 0;
                while(rs.next())
                    count = rs.getInt(1);
                if(count > 0)
                    JOptionPane.showMessageDialog(null, "Aynı isimde bir bölüm var");
                else
                {
                    sql = "INSERT INTO TBLDEPARTMENT( DEPAD ) VALUES ('" +jTextFieldDepName.getText()  + "')";
                    db.executeNonQuery(sql);
                    fillDepartmentsjList();
                    
                }
                    
            }
        } catch (SQLException ex) {
            Logger.getLogger(DepartmentsGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonDepAddMouseClicked

    private void jButtonDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonDeleteMouseClicked
        Item item = jListDepartments.getSelectedValue();
        if(item == null)
            JOptionPane.showMessageDialog(null, "Silmek için bir bölüm seçiniz");
        else
        {
            try {
                String connection = "jdbc:hsqldb:file:db/Faculty";
                String sql = "DELETE FROM TBLDEPARTMENT WHERE DEPID=" + item.getId();
                System.out.println(sql);
                DBHandler db = new DBHandler(connection);
                
                int dialogButton = JOptionPane.YES_NO_OPTION;
                int dialogResult = JOptionPane.showConfirmDialog(null,"Silmek istediğinize emin misiniz?", "Uyarı", dialogButton);
                if(dialogResult == JOptionPane.YES_OPTION)
                {
                    db.executeNonQuery(sql);
                    fillDepartmentsjList();
                }
                
                
            } catch (SQLException ex) {
                Logger.getLogger(DepartmentsGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonDeleteMouseClicked

